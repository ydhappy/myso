SET NAMES utf8;

-- ------------------------------------------------------------
-- 0. 기본 변수
-- ------------------------------------------------------------
SET @캐릭터번호 := 100001;
SET @아이템번호 := 123456789;
SET @이름 := '카르마';
SET @성격 := '수호';
SET @형태 := 'sword';
SET @능력 := 'EGO_BALANCE';
SET @인벤이미지 := 0;
SET @바닥이미지 := 0;
SET @표시 := '한손검';
SET @설명 := '에고 한손검 형태';

-- 형태: dagger 단검 / sword 한손검 / tohandsword 양손검 / axe 도끼 / spear 창 / bow 활 / staff 지팡이 / wand 완드
-- 이미지: 0이면 원본 아이템 이미지 사용, 1 이상이면 해당 gfx 번호 사용

-- ------------------------------------------------------------
-- 1. 에고 생성 또는 재활성화
-- ------------------------------------------------------------
INSERT INTO `에고`
(`아이템번호`, `캐릭터번호`, `사용`, `이름`, `성격`, `레벨`, `경험치`, `필요경험치`, `대화단계`, `제어단계`, `마지막대화`, `마지막경고`, `형태`, `이전방패`)
VALUES
(@아이템번호, @캐릭터번호, 1, @이름, @성격, 1, 0, 100, 1, 1, 0, 0, @형태, 0)
ON DUPLICATE KEY UPDATE
    `캐릭터번호` = VALUES(`캐릭터번호`),
    `사용` = 1,
    `이름` = VALUES(`이름`),
    `성격` = VALUES(`성격`),
    `형태` = VALUES(`형태`),
    `레벨` = IF(`레벨` < 1, 1, `레벨`),
    `필요경험치` = IF(`필요경험치` < 1, 100, `필요경험치`),
    `대화단계` = IF(`대화단계` < 1, 1, `대화단계`),
    `제어단계` = IF(`제어단계` < 1, 1, `제어단계`);

-- ------------------------------------------------------------
-- 2. 능력 설정, 한 무기당 활성 능력 1개
-- ------------------------------------------------------------
UPDATE `에고능력`
SET `사용` = 0
WHERE `아이템번호` = @아이템번호;

INSERT INTO `에고능력`
(`아이템번호`, `능력`, `레벨`, `확률보너스`, `피해보너스`, `마지막발동`, `사용`)
VALUES
(@아이템번호, @능력, 1, 0, 0, 0, 1)
ON DUPLICATE KEY UPDATE
    `사용` = 1,
    `레벨` = IF(`레벨` < 1, 1, `레벨`);

-- ------------------------------------------------------------
-- 3. 형태별 인벤토리/바닥 이미지 설정
-- ------------------------------------------------------------
INSERT INTO `에고모양`
(`형태`, `표시`, `인벤이미지`, `바닥이미지`, `설명`, `사용`)
VALUES
(@형태, @표시, @인벤이미지, @바닥이미지, @설명, 1)
ON DUPLICATE KEY UPDATE
    `표시` = VALUES(`표시`),
    `인벤이미지` = VALUES(`인벤이미지`),
    `바닥이미지` = VALUES(`바닥이미지`),
    `설명` = VALUES(`설명`),
    `사용` = 1;

-- 예시:
-- UPDATE `에고모양` SET `인벤이미지` = 1001, `바닥이미지` = 1002 WHERE `형태` = 'bow';
-- UPDATE `에고모양` SET `인벤이미지` = 0, `바닥이미지` = 0 WHERE `형태` = 'bow'; -- 원본 이미지 사용

-- ------------------------------------------------------------
-- 4. 현재 상태 조회
-- ------------------------------------------------------------
SELECT
    e.`아이템번호`, e.`캐릭터번호`, e.`사용`, e.`이름`, e.`성격`, e.`형태`, e.`이전방패`,
    e.`레벨`, e.`경험치`, e.`필요경험치`, e.`대화단계`, e.`제어단계`,
    a.`능력`, a.`레벨` AS `능력레벨`, a.`확률보너스`, a.`피해보너스`,
    m.`표시`, m.`인벤이미지`, m.`바닥이미지`, m.`설명`,
    e.`수정일`
FROM `에고` e
LEFT JOIN `에고능력` a ON a.`아이템번호` = e.`아이템번호` AND a.`사용` = 1
LEFT JOIN `에고모양` m ON m.`형태` = e.`형태` AND m.`사용` = 1
WHERE e.`아이템번호` = @아이템번호;

