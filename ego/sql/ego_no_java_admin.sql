SELECT
    e.`캐릭터번호`,
    e.`아이템번호`,
    e.`사용`,
    e.`이름`,
    e.`성격`,
    e.`형태`,
    e.`레벨`,
    e.`경험치`,
    e.`필요경험치`,
    a.`능력`,
    a.`레벨` AS `능력레벨`,
    a.`사용` AS `능력사용`,
    e.`수정일`
FROM `에고` e
LEFT JOIN `에고능력` a
       ON a.`아이템번호` = e.`아이템번호`
      AND a.`사용` = 1
ORDER BY e.`수정일` DESC, e.`아이템번호` DESC;

-- ------------------------------------------------------------
-- 7. 이상 데이터 점검
-- ------------------------------------------------------------
-- 에고는 켜져 있는데 능력이 없음
SELECT e.*
FROM `에고` e
LEFT JOIN `에고능력` a
       ON a.`아이템번호` = e.`아이템번호`
      AND a.`사용` = 1
WHERE e.`사용` = 1
  AND a.`번호` IS NULL;

-- 활성 능력이 2개 이상
SELECT `아이템번호`, COUNT(*) AS `활성능력수`
FROM `에고능력`
WHERE `사용` = 1
GROUP BY `아이템번호`
HAVING COUNT(*) > 1;

-- 비정상 값
SELECT *
FROM `에고`
WHERE `레벨` < 1
   OR `필요경험치` < 1
   OR `경험치` < 0
   OR `형태` NOT IN ('', 'dagger', 'sword', 'tohandsword', 'axe', 'spear', 'bow', 'staff', 'wand');
